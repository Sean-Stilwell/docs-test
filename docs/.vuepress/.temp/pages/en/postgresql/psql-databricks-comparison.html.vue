<h2 id="introduction" tabindex="-1"><a class="header-anchor" href="#introduction"><span>Introduction</span></a></h2>
<p>When it comes to hosting a database with FSDH, there are multiple options available. One popular choice is using PostgreSQL, while another option is leveraging the database features provided by Azure Databricks. In this comparison, we will explore the key differences and benefits of each approach.</p>
<h2 id="postgresql" tabindex="-1"><a class="header-anchor" href="#postgresql"><span>PostgreSQL</span></a></h2>
<p>PostgreSQL is a fully managed database service that allows you to deploy PostgreSQL databases in the cloud. It provides high availability, automatic backups, and integration with other services. Some of the key features of PostgreSQL include:</p>
<ul>
<li>Easy deployment of PostgreSQL databases</li>
<li>Built-in high availability and automatic backups</li>
<li>Support for advanced PostgreSQL features such as JSONB, full-text search, and spatial data types</li>
<li>Seamless integration with other services like web apps or APIs</li>
</ul>
<h2 id="azure-databricks-database-features-hive" tabindex="-1"><a class="header-anchor" href="#azure-databricks-database-features-hive"><span>Azure Databricks Database Features (Hive)</span></a></h2>
<p>Azure Databricks is an analytics platform that combines Apache Spark with a collaborative environment for data science and machine learning. While it is primarily known for its big data processing capabilities, Azure Databricks also provides powerful database features. Some of the key database features offered by Azure Databricks include:</p>
<ul>
<li>Delta Lake: A transactional storage layer that provides ACID transactions, schema enforcement, and data versioning for data lakes.</li>
<li>Delta Engine: A high-performance query engine optimized for Delta Lake, enabling fast and scalable SQL queries on large datasets.</li>
<li>Delta Live Tables: A real-time change data capture (CDC) feature that allows you to capture and process real-time data changes from various sources.</li>
<li>Data Lakehouse Architecture: Azure Databricks promotes a unified architecture that combines the best of data lakes and data warehouses, enabling efficient data storage, processing, and analytics.</li>
</ul>
<p>Databricks leverages the standard <a href="https://hive.apache.org/" target="_blank" rel="noopener noreferrer">Apache Hive</a> project which enables to leverage standard storage and query data using SQL. The underlying model is quite different from a traditional database.</p>
<ul>
<li>Databricks SQL requires the cluster to be enabled to run SQL queries</li>
<li>Large datasets can be easily accomodated by simply scaling the cluster size</li>
<li>There is only storage costs for data that is not used</li>
<li>Large datasets are cost efficient because standard Azure storage is used.</li>
</ul>
