<li>
<p>Click on the <code v-pre>Invite New User</code> button
<img src="@source/en/managing-workspaces-and-users/img/invite-new-user.png" alt="invite new user"></p>
</li>
<li>
<p>Enter the Government of Canada email address of the user you want to invite and click on the dropdown.
<img src="@source/en/managing-workspaces-and-users/img/click-add-new-user.png" alt="click add new user"></p>
