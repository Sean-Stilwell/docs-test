<h2 id="overview" tabindex="-1"><a class="header-anchor" href="#overview"><span>Overview</span></a></h2>
<p>As part of the Federal Science Datahub, we provide custom Databricks cluster policies that are designed to help you get the most out of your Databricks clusters by having predefined cluster configurations. We currently offer 3 cluster policies on top of Databrick's personal compute policy. These are &quot;Datahub Small Cluster&quot;, &quot;Datahub Regular Cluster&quot; and &quot;Datahub Large Cluster&quot;. While workers and drivers configurations are predefined, the choice of <a href="https://docs.databricks.com/runtime/index.html" target="_blank" rel="noopener noreferrer">databricks runtime</a> is completely up to you. We will go through each of these cluster policies in detail below.</p>
<h3 id="personal-compute" tabindex="-1"><a class="header-anchor" href="#personal-compute"><span><strong>Personal Compute</strong></span></a></h3>
<p>This is the default cluster policy that is provided by Databricks. It is designed for personal use and is not recommended for production use. It is a good choice if you are just getting started with Databricks and want to get a feel for how it works. It is also a good choice if you are working with small datasets and do not need a lot of compute power. Do note that this cluster is a single-node/single-worker cluster and as such no parallelization will be done. By default, it uses the latest machine learning runtime, meaning machine learning toolings are available within this cluster. The cluster configuration is as follows:</p>
<ul>
<li><strong>Node type:</strong> Standard_DS3_v2 (4 CPU, 14 GB memory)</li>
</ul>
<p><a href="https://docs.databricks.com/clusters/personal-compute.html" target="_blank" rel="noopener noreferrer">Read more here on personal compute policies</a></p>
<h3 id="datahub-small-cluster" tabindex="-1"><a class="header-anchor" href="#datahub-small-cluster"><span><strong>Datahub Small Cluster</strong></span></a></h3>
<p>This cluster policy is designed for small production workloads. It is a good choice if you are working with small datasets and do not need a small amount of compute power that can scale if needed. It is also a good choice if you are just getting started with Databricks and want to get a feel for how non-personal cluster. The cluster configuration is as follows:</p>
<ul>
<li><strong>Worker and driver type:</strong> Standard_D4ds_v5 (4 CPU, 16 GB memory)</li>
<li><strong>Amount of workers:</strong> 0 to 2 workers</li>
<li><strong>Can do spot instances</strong></li>
<li><strong>Can do autoscaling</strong></li>
</ul>
<h3 id="datahub-regular-cluster" tabindex="-1"><a class="header-anchor" href="#datahub-regular-cluster"><span><strong>Datahub Regular Cluster</strong></span></a></h3>
<p>This cluster policy is designed for regular production workloads. It is a good choice if you've encountered bottlenecks with the small cluster configuration. The cluster configuration is as follows:</p>
<ul>
<li><strong>Worker and driver type:</strong> Choice of the following
<ul>
<li>Standard_D4ds_v5 (4 CPU, 16 GB memory)</li>
<li>Standard_D8ds_v5 (8 CPU, 32 GB memory)</li>
<li>Standard_D16ds_v5 (16 CPU, 64 GB memory)</li>
</ul>
</li>
<li><strong>Amount of workers:</strong> 0 to 4 workers</li>
<li><strong>Can do spot instances</strong></li>
<li><strong>Can autoscale</strong></li>
</ul>
<h3 id="datahub-large-cluster" tabindex="-1"><a class="header-anchor" href="#datahub-large-cluster"><span><strong>Datahub Large Cluster</strong></span></a></h3>
<p>This cluster policy is designed for large production workloads. It is a good choice if you're working with extremely big data that requires maximal parallelization. It is not recommended users use this cluster unless they are aware of the possible costs it may entail. The cluster configuration is as follows:</p>
<ul>
<li><strong>Worker and driver type:</strong> Choice of the following
<ul>
<li>Standard_D4ds_v5 (4 CPU, 16 GB memory)</li>
<li>Standard_D8ds_v5 (8 CPU, 32 GB memory)</li>
<li>Standard_D16ds_v5 (16 CPU, 64 GB memory)</li>
<li>Standard_D32ds_v5 (32 CPU, 128 GB memory)</li>
<li>Standard_D48ds_v5 (48 CPU, 192 GB memory)</li>
<li>Standard_D64ds_v5 (64 CPU, 256 GB memory)</li>
</ul>
</li>
<li><strong>Amount of workers:</strong> 0 to 4 workers</li>
<li><strong>Can do spot instances</strong></li>
<li><strong>Can autoscale</strong></li>
</ul>
